package david.hoyos.xpertgroup.dominio;

public class Coordenada {
	
	Integer x;
	Integer y;
	Integer z;
	
	public Coordenada(Integer x,Integer y,Integer z) {
		this.x = x;
		this.y= y;
		this.z =z;
	}

	public Integer getX() {
		return x;
	}

	public Integer getY() {
		return y;
	}

	public Integer getZ() {
		return z;
	}
}
